    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if(val()==true){ 
        try {
            pst = con.prepareStatement("SELECT emp_lname, emp_fname FROM hris_emp WHERE emp_id=?");
            int id = Integer.parseInt(searchData.getText());
            pst.setInt(1, id);
            ResultSet rs1 = pst.executeQuery();
            if (rs1.next() == false) {
                JOptionPane.showMessageDialog(null, "Sorry Record Not found");
                lname.setText(rs1.getString(""));
                searchData.requestFocus();
            } else {
            lname.setText(rs1.getString("emp_lname"));
            fname.setText(rs1.getString("emp_fname"));
            
            }
        } catch (SQLException ex) {
          JOptionPane.showMessageDialog(null, ex);
        }
         }
    }//GEN-LAST:event_jButton5ActionPerformed

